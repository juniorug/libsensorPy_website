				<h2>Frequently Asked Questions</h2>

                <p><em>How can I find out if I or my child has been tested?</em><br/>
                   If you were born in California after 1990, you have been tested.  You may want to contact your pediatrician, birth hospital, or doctor's office to get the result.  If you have children, your doctor may have tested you when you were expecting.</p>
				<br/>
               	<p><em>What kinds of sickle cell tests are there?</em><br/>
                  The two most common are "Hemoglobin electrophoresis with a CBC" and the solubility, or "SickleDexTM"  The hemoglobin electrophoresis with the CBC is a more complete test, as it tests for most of the hemoglobin types (there are over 200).  The solubility test does test for hemoglobin S, or sickle, but does not test for the other hemoglobin types.  If a person has another type of hemoglobin trait, this test type will not show it.</p>  
				<br/><br/>
<p><em>What if I had a solubility test?</em><br/>
                    This means you have been tested for sickle cell disease and trait, but if you happen to be a carrier of another type of hemoglobin it will not show from this test.</p>
				<br/>
               	<p><em>How can I find out my hemoglobin type?</em></p>
               	<ul>
                    <li>If you were born in CA after 1990, contact your birth hospital, pediatrician or doctor's office. The California Department of Public Health (CDPH) will also have this information.</li>  
                    <li>If you were born in another state, contact your doctor or that state's Department of Public Health, as it changes from state to state when they started testing all babies for sickle cell disease and trait.</li>  
	 		   	</ul>
                <br/>
			   	<p><em>What if I have sickle cell trait?</em></p>
               	<ul>
                    <li>You can live a long, normal, healthy life.  Most people who have trait might not even know they have it.</li>  
                    <li>You might pass it on to your child.</li> 
                    <li>If you and your partner both have sickle cell or other hemoglobin traits, it is recommended that you contact a sickle cell counselor, genetic counselor or a CCS  approved Sickle Cell Disease Special Care Center for more information as you are at risk for having a child with sickle cell disease.</li> 
				</ul>
