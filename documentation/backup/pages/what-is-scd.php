                <h2>Common symptoms associated with sickle cell disease include:</h2>
                <p>Pain; acute chest syndrome; pulmonary hypertension; increased susceptibility to infection; protein in the urine; priapism; gall stones; bone disease; skin ulcers; severe anemia; problems with the spleen (splenic sequestration); brain injury; and problems with the eyes (retinopathy).<br/><br/>
                The prevalence of hemoglobin S is high in the African American population, 1:14; the prevalence of sickle cell disease of all kinds is about 1:350 African Americans.  The prevalence of hemoglobin S in the Hispanic population is less, about 1:1000; the prevalence of the disease in this population is not known.<br/><br/>  
The sickle mutation occurs in peoples from the Mediterranean, India, and other regions.  Sickle cell trait occurs in Caucasians as well; in this group symptoms can be more severe than in other groups.  It is believed that there are 100,000 people with sickle cell disease in the United States.</p> 
