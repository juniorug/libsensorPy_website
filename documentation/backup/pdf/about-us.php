                <p>The Comprehensive Sickle Cell Center at Children's Hospital & Research Center Oakland was started in 1978 to deliver comprehensive care to children and adults with sickle cell disease. The sickle cell center grew quickly and was funded by the National Institutes of Health as one of ten Comprehensive Sickle Cell Centers in the country.<br/>
                   The center has been active in research nationally, conducting large multi-center trials in sickle cell disease and internationally, providing our expertise to programs abroad.</p>
                <img class="float_left" src="../img/hemacia_small.png"/> 
                <p>Sickle cell disease is complex and requires a comprehensive approach.  The care at Children's Hospital is all inclusive, focusing on patients and their families through all of the stages of their lives - from infancy, through adolescence, to a successful adulthood. While pediatric and adult programs have separate staffs, there are also shared staff who bridge the transition to adult care.  Center providers are experts in sickle cell disease, transfusion medicine, and bone marrow transplantation. Our hemoglobinopathy laboratory confirms all hemoglobin disorders results for the State of California Newborn Screening Program, and sets standards for other national and international laboratories.  Our center collaborates in national studies to promote better treatment for sickle cell disease and is recognized as an innovator for new therapies.</p>
