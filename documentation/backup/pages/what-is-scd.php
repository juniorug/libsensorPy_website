                	<li>Sickle cell trait can only be inherited- in other words, something a parent passes to a child like eye or hair color.</li>
               		<li>Sickle cell trait is not contagious- you can not "catch" it.</li>
               		<li>Sickle cell trait will never turn into sickle cell disease.  It is part of your DNA so it will not change just like your eye color will not change on its own.</li>
               		<li>Most people with sickle cell trait (or "carriers" for sickle cell) do not ever have medical problems associated with trait.  </li>
