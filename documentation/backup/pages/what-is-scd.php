                <h2>What is Sickle Cell Disease?</h2>
                
                <p>Sickle cell disease refers to a group of genetic disorders of hemoglobin, a protein in red blood cells that carry oxygen. The conditions cause a change in hemoglobin A that create hemoglobin S. "Sickle cell anemia" refers to the condition where both hemoglobins are S (hemoglobin SS). Sickle cell disease refers to hemoglobin S in combination with another type of hemoglobin, such as C, D or E. The most common sickle combinations are: hemoglobins SS, SC, and S beta zero or S beta plus thalassemia.  Hemoglobin S occurring with hemoglobin A as the dominant hemoglobin (AS) is sickle cell trait and is not a disease.</p>
                
