                <h2>The center provides:</h2>
                
                
                <ul>
                    <li>Primary through tertiary care</li>
                    <li>Transplantation for sickle cell disease</li>
                    <li>Diagnostic and screening testing for complications of sickle cell disease</li>
                    <li>Education, outreach and genetic counseling</li>
                    <li>Psychosocial services for patients and families</li>
                </ul>
                <h2>Facilities:</h2>
                <ul>
                    <li>Our twenty bed day hospital provides transfusion services including automated exchange transfusion, pain management and rooms for the evaluation of patients following bone marrow transplant.</li>                 
                    <li>25 inpatient hospital beds for infancy through age 21 with a state of the art bone marrow transplant unit. Adults are admitted to partner community hospitals.</li>                
                    <li>A family house for families of patients traveling a distance or who require extended therapy.</li>
                </ul>
