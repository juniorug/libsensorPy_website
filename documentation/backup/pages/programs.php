    <h2>Emergency Department Quality Improvement Project</h2>
    <p>The Network of Care, with support from the <strong>National Initiative for Children&acute;s Healthcare Quality (NICHQ)</strong> has initiated a quality improvement project with the aims of improving SCD pain management in the Emergency Department. Introducing pain management materials, educating ED staff about effective treatment for SCD pain, and improving patient and family satisfaction with treatment in the ED are a few of the aims of this particular project. The emergency department at <strong>Children&acute;s Hospital & Research Center Oakland</strong> has initiated the first of this kind of project in the Northern California area. Along with other institutions in the country, the Network of Care hopes to improve pain management for people living with SCD across the entire nation.</p> 
    
    
    <h2>Adherence with Iron Chelation Therapy Quality Improvement Project</h2>
    <p>Along the lines of quality improvement, the Network of Care and NICHQ have also introduced a project aimed at improving adherence with iron chelation therapy. Iron overload continues to be an overwhelming problem for people living with SCD who receive regular blood transfusions. Unfortunately, few therapies exist for iron overload. Deferasirox or Exjade is a medication that, when dissolved into a liquid form or mixed with food, can effectively treat iron overload. However, a growing issue among people with SCD is the lack of adherence with Exjade. Poor adherence rates lead to poor treatment of iron overload resulting in poor quality of life.</p> 
    <br/>
    <p><strong>Vitality&trade;, Avella Specialty Pharmacy</strong>, and the Network of Care have partnered together to introduce an electronic device, the GlowCap&trade;, which when programmed sounds an alarm at specified times alerting individuals to take their medication. The GlowCap&trade; replaces the cap on a normal prescription bottle and works remotely with a router plugged into the wall. When programmed a signal is sent to the GlowCap&trade; allowing it to omit a light and sound alerting the individual to take their medication. Once the cap is opened and closed, a signal is sent back to Vitality&trade;. At the end of each week a report can be sent to individuals, their physicians, and family members displaying their adherence for that week.</p> 
   <br/> 
  <p>With help from Children&acute;s Hospital & Research Center Oakland the GlowCap&trade; project is one of the first of its kind being offered for SCD treatment not only in the Northern California region but across the country as well.</p>       
