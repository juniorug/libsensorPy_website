                <h2>Sickle Cell Facts:</h2>
                <ul>
                    <li>The sickle gene affects the red blood cells</li><br/>
                    <li>Sickle cell trait IS NOT sickle cell disease, and will NOT turn into sickle cell disease</li><br/>
                    <li>A person with sickle cell trait inherited one gene for regular hemoglobin (A) from one parent and one gene for sickle hemoglobin (S) from the other</li><br/> 
                    <li>A person with sickle cell disease inherited a gene FROM EACH PARENT that caused the disease</li>
                </ul>
