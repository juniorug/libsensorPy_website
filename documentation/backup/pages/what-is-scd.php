				<ul>
                    <li>Sickle cell disease is not contagious</li>
                    <li>It is passed down in a recessive pattern from biological parents</li> 
                    <li>If two parents have trait, they may have a child with sickle cell disease</li>                    
                    <li>One parent with disease and one parent with trait may also have a child with disease</li>
