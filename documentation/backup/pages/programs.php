    <p>Our focus is to increase access to special health care for youth and adults, including adults not already receiving special sickle cell disease care. We offer support for our patients with peer education, classes about positive health care and the medical home model.  We offer genetic counseling and social services to reduce barriers to accessing health care. </p>
